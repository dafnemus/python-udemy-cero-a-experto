# Indices y slicing

# Ejemplo con cadena:
TEXTO = 'Hola Mundo'
print(TEXTO)
# indice
print(TEXTO[0], TEXTO[1], TEXTO[2])

# indice inverso
print(TEXTO[-3], TEXTO[-2], TEXTO[-1])

# slicing:
print(TEXTO[0:4])

# con omisi√≥n:
print(TEXTO[:3])

# inverso
print(TEXTO[-6:2])
