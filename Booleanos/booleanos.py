# pylint: disable=missing-docstring

# Booleanos: True or False.

SUMA = 1 + 1

# True
print(SUMA == 2)

# False
print(SUMA == 3)

# True and True

verdad = (SUMA == 2) and (SUMA + 1 == 3)

print(verdad)

# Tipo de Dato.
print(type(verdad))

# True and False

falsedad = (SUMA == 2) and (SUMA + 2 == 3)
print(falsedad)
