# Operadores Lógicos:

# AND:
VERDAD = True and True
print(VERDAD)

FALSEDAD = False and False
print(FALSEDAD)

FALSEDAD = True and False
print(FALSEDAD)

# OR:
VERDAD = True or False
print(VERDAD)

VERDAD = True or True
print(VERDAD)

FALSEDAD = False or False
print(FALSEDAD)


# NOT, negación:
FALSEDAD = not True
print(FALSEDAD)

VERDAD = not False
print(VERDAD)
